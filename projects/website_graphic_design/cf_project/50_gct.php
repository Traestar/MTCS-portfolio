        <h1 class="regular brown bottom_line">About this study:</h1>
        <p> CFRD can often be difficult to initially diagnosis because the condition may not always display symptoms.  
			Due to this, it is recommended that patients with CF undergo a 75gram Oral Glucose Tolerance Test (OGTT) 
			annually in order to screen for CFRD.   This test, however, can be time consuming and inconvenient, resulting 
			in a lack of adherence to the screening recommendations.<br/>
		</p>
		<p>
			The 50 glucose challenge test (GCT) does not require fasting, and blood is drawn only at the 1 hour mark, 
			making it less cumbersome than the 75 gram OGTT.  This study will help determine whether the 50 gram GCT 
			is a better alternative for screening for CFRD.  It will also examine the relationship between the glucose 
			tolerance tests and lung function, growth, weight, height and muscle in cystic fibrosis.<br/>
		</p>
		<p>
			This information may increase provider understanding of the relationship between glucose abnormalities and
			worse clinical outcomes, and allow for easier and more accurate screening of CFRD.<br/>   
        </p>		
